// One of an arrays built in properties is lenght and it returns the items of the array
// We access the .length property just like we do with strings

const newYearsResolutions = ["Keep a journal", "Take a falconry class"];
console.log(newYearsResolutions.length);

// In the example above, we log newYearsResolutions.length to the console using the following steps:

/**
 * We use dot notation, chaining a period withe the property name to the array, to access the length property of the newYearsResolutions array.
 * Then we log the length of newYearsResolutions to the console.
 * Since newYearsResolutions has two elements, so 2 would be logged to the console.
 */

// When we want to know how many elements are in an array, we can access the .length property

const objectives = ["Learn a new languages", "Read 52 books", "Run a marathon"];
console.log(objectives.length);
